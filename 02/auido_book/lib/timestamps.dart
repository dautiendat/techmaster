class Timestamp {
  final int startTime;    
  final int duration;     
  final int isPunctuation;
  final String text;      
  final int startIndex;   
  final int length;       

  Timestamp({
    required this.startTime,
    required this.duration,
    required this.isPunctuation,
    required this.text,
    required this.startIndex,
    required this.length,
  });
  
}
  